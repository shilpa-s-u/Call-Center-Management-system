class User6 {
    private String pid,ptype,pdate,status;

    public User6(String pid, String ptype, String pdate, String status) {
        this.pid = pid;
        this.ptype = ptype;
        this.pdate = pdate;
        this.status = status;
    }

    public String getPid() {
        return pid;
    }

    public String getPtype() {
        return ptype;
    }

    public String getPdate() {
        return pdate;
    }

    public String getStatus() {
        return status;
    }

  

    
    
}
